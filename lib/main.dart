class MyApp
    extends
    StatelessWidget {
  const MyApp(
      {Key? key})
      : super(
      key: key
  );



  @override
  Widget build(
      BuildContext
      context
      ) {
    return
      MultiProvider(
      providers: [
        // this is call bloc movie
        BlocProvider(
          create: (_)
          => di
              .locator<
              MovieDetailBloc>(
          ),
        ),
        BlocProvider(
          create: (_)
          => di
              .locator<
              MoviePopularBloc>(
          ),
        ),
        BlocProvider(
          create: (_)
          => di
              .locator<
              MovieRecommendationBloc>(
          ),
        ),
        BlocProvider(
          create: (_)
          => di
              .locator<
              MovieSearchBloc>(
          ),
        ),
        BlocProvider(
          create: (_)
          => di
              .locator<
              MovieTopRatedBloc>(
          ),
        ),
        BlocProvider(
          create: (_)
          => di
              .locator<
              MovieNowPlayingBloc>(
          ),
        ),
        BlocProvider(
          create: (_)
          => di
              .locator<
              MovieWatchlistBloc>(
          ),
        ),

        // this is call bloc tv
        BlocProvider(
          create: (_) => di
              .locator<
              TvseriesDetailBloc>(
          ),
        ),
        BlocProvider(
          create: (_)
          => di
              .locator<
              TvseriesRecommendationsBloc>(
          ),
        ),
        BlocProvider(
          create: (_)
          => di
              .locator<
              TvseriesSearchBloc>(
          ),
        ),
        BlocProvider(
          create: (_)
          => di
              .locator<
              PopularTvseriesBloc>(
          ),
        ),
        BlocProvider(
          create: (_)
          => di
              .locator<
              TopRatedTvseriesBloc>(
          ),
        ),
        BlocProvider(
          create: (_)
          => di
              .locator<
              OnTheAirTvseriesBloc>(
          ),
        ),
        BlocProvider(
          create: (_)
          => di
              .locator<
              WatchlistTvseriesBloc>(
          ),
        ),
      ],

      child: MaterialApp(
        title: 'Flutter Demo',
        theme: ThemeData
            .dark()
            .copyWith(
          colorScheme:
          kColorScheme,
          primaryColor:
          kRichBlack,
          scaffoldBackgroundColor:
          kRichBlack,
          textTheme:
          kTextTheme,
        ),
        home:  HomePage(
        ),
        navigatorObservers: [
          routeObserver],
        onGenerateRoute: (
            RouteSettings
            settings
            ) {
          switch (
          settings
              .name) {
            case
            HomePage
                .ROUTE_NAME:
              return
                MaterialPageRoute(
                    builder: (_)
              => const
              HomePage()
              );


            case
            PopularMoviesPage
                .ROUTE_NAME:
              return CupertinoPageRoute(
                  builder: (_)
                  => PopularMoviesPage()
              );


            case
            TopRatedMoviesPage
                .ROUTE_NAME:
              return CupertinoPageRoute(
                  builder: (_)
                  => TopRatedMoviesPage()
              );


            case
            SearchPage
                .ROUTE_NAME:
              return CupertinoPageRoute(
                  builder: (_)
                  => const SearchPage()
              );


            case
            MovieDetailPage
                .ROUTE_NAME:
              final
              id
              = settings
                  .arguments
              as int;

              return
                MaterialPageRoute(
                builder: (_)
                => MovieDetailPage(
                    id: id
                ),
                settings:
                settings,
              );


            case
            HomeTvSeriesPage
                .ROUTE_NAME:
              return MaterialPageRoute(
                  builder: (_)
                  => HomeTvSeriesPage()
              );


            case
            PopularTvSeriesPage
                .ROUTE_NAME:
              return CupertinoPageRoute(
                  builder: (_)
                  => PopularTvSeriesPage()
              );


            case
            TopRatedTvSeriesPage
                .ROUTE_NAME:
              return CupertinoPageRoute(
                  builder: (_)
                  => TopRatedTvSeriesPage()
              );


            case
            SearchPageTvSeries
                .ROUTE_NAME:
              return CupertinoPageRoute(
                  builder: (_)
                  => SearchPageTvSeries()
              );


            case
            TvSeriesDetailPage
                .ROUTE_NAME:
              final
              id
              = settings
                  .arguments
              as int;


              return
                MaterialPageRoute(
                builder: (_)
                => TvSeriesDetailPage(
                    id: id
                ),
                settings:
                settings,
              );

            case
            WatchlistPage
                .ROUTE_NAME:
              return MaterialPageRoute(
                  builder: (_)
                  => const WatchlistPage()
              );


            case
            AboutPage
                .ROUTE_NAME:
              return
                MaterialPageRoute(
                    builder: (_)
                    => const AboutPage()
                );


            default:
              return
                MaterialPageRoute(
                    builder: (_) {
                return
                  const Scaffold(
                  body: Center(
                    child: Text(
                        'Page not found :('
                    ),
                  ),
                );
              });
          }
        },
      ),
    );
  }
}
